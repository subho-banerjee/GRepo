
public class Assignment2b {

	/**
	 * if-else-if ladder
	 */
	public static void main(String[] args) {
		int value = 1;
		if(value == 1)
			System.out.println("Hello");
		else if(value == 2)
			System.out.println("Hi");
		else if(value == 3)
			System.out.println("Good");
		else
			System.out.println("No match found");

	}

}
